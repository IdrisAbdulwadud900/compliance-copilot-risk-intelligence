MIGRATIONS: tuple[tuple[int, str, MigrationHandler], ...] = (
    (1, "core_schema", _migration_001_core_schema),
    (2, "alerts_incidents_webhooks", _migration_002_alerts_and_incidents),
    (3, "cases_and_analysis_metadata", _migration_003_cases_and_analysis_metadata),
)


def available_migration_versions() -> tuple[int, ...]:
    return tuple(version for version, _, _ in MIGRATIONS)


def apply_migrations(db_path: str | None = None) -> None:
    with database_connection(db_path) as conn:
        _ensure_migration_table(conn)
        applied = _applied_versions(conn)
        for version, name, handler in MIGRATIONS:
            if version in applied:
                continue
            handler(conn)
            _record_migration(conn, version, name)
        conn.commit()


def get_applied_migration_versions(db_path: str | None = None) -> tuple[int, ...]:
    with database_connection(db_path) as conn:
        versions = sorted(_applied_versions(conn))
    return tuple(versions)


def migration_status_summary(db_path: str | None = None) -> dict[str, object]:
    available = available_migration_versions()
    applied = get_applied_migration_versions(db_path)
    pending = tuple(version for version in available if version not in applied)
    return {
        "applied_versions": list(applied),
        "pending_versions": list(pending),
        "current_version": max(applied, default=0),
        "available_version": max(available, default=0),
        "up_to_date": not pending,
    }
