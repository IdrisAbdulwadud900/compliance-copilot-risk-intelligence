from __future__ import annotations

from typing import Callable

from app.storage.runtime import DatabaseConnection, database_connection


MigrationHandler = Callable[[DatabaseConnection], None]


def _id_column_sql(conn: DatabaseConnection) -> str:
    if conn.backend == "postgres":
        return "BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY"
    return "INTEGER PRIMARY KEY AUTOINCREMENT"


def _column_exists(conn: DatabaseConnection, table_name: str, column_name: str) -> bool:
    if conn.backend == "postgres":
        row = conn.execute(
            """
            SELECT 1
            FROM information_schema.columns
            WHERE table_schema = 'public' AND table_name = ? AND column_name = ?
            """,
            (table_name, column_name),
        ).fetchone()
        return row is not None

    columns = conn.execute(f"PRAGMA table_info({table_name})").fetchall()
    return any(row[1] == column_name for row in columns)


def _ensure_migration_table(conn: DatabaseConnection) -> None:
    conn.execute(
        """
        CREATE TABLE IF NOT EXISTS schema_migrations (
            version INTEGER PRIMARY KEY,
            name TEXT NOT NULL,
            applied_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
        )
        """
    )


def _applied_versions(conn: DatabaseConnection) -> set[int]:
    _ensure_migration_table(conn)
    rows = conn.execute("SELECT version FROM schema_migrations").fetchall()
    return {int(row[0] if isinstance(row, tuple) else row["version"]) for row in rows}


def _record_migration(conn: DatabaseConnection, version: int, name: str) -> None:
    conn.execute(
        "INSERT INTO schema_migrations (version, name) VALUES (?, ?)",
        (version, name),
    )


def _migration_001_core_schema(conn: DatabaseConnection) -> None:
    conn.execute(
        f"""
        CREATE TABLE IF NOT EXISTS analyses (
            id {_id_column_sql(conn)},
            tenant_id TEXT NOT NULL DEFAULT 'legacy',
            created_at TEXT NOT NULL,
            chain TEXT NOT NULL DEFAULT 'ethereum',
            address TEXT NOT NULL,
            txn_24h INTEGER NOT NULL,
            volume_24h_usd REAL NOT NULL,
            sanctions_exposure_pct REAL NOT NULL,
            mixer_exposure_pct REAL NOT NULL,
            bridge_hops INTEGER NOT NULL,
            score INTEGER NOT NULL,
            risk_level TEXT NOT NULL,
            explanation TEXT NOT NULL
        )
        """
    )
    if not _column_exists(conn, "analyses", "tenant_id"):
        conn.execute("ALTER TABLE analyses ADD COLUMN tenant_id TEXT NOT NULL DEFAULT 'legacy'")
    if not _column_exists(conn, "analyses", "chain"):
        conn.execute("ALTER TABLE analyses ADD COLUMN chain TEXT NOT NULL DEFAULT 'ethereum'")
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_analyses_tenant_created ON analyses(tenant_id, created_at)"
    )

    conn.execute(
        f"""
        CREATE TABLE IF NOT EXISTS users (
            id {_id_column_sql(conn)},
            email TEXT NOT NULL UNIQUE,
            password_hash TEXT NOT NULL,
            tenant_id TEXT NOT NULL,
            role TEXT NOT NULL DEFAULT 'admin',
            created_at TEXT NOT NULL
        )
        """
    )

    conn.execute(
        f"""
        CREATE TABLE IF NOT EXISTS audit_logs (
            id {_id_column_sql(conn)},
            tenant_id TEXT NOT NULL,
            created_at TEXT NOT NULL,
            actor_email TEXT NOT NULL,
            action TEXT NOT NULL,
            target TEXT NOT NULL,
            details TEXT NOT NULL
        )
        """
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_audit_tenant_created ON audit_logs(tenant_id, created_at)"
    )

    conn.execute(
        f"""
        CREATE TABLE IF NOT EXISTS invites (
            id {_id_column_sql(conn)},
            token TEXT NOT NULL UNIQUE,
            email TEXT NOT NULL,
            tenant_id TEXT NOT NULL,
            role TEXT NOT NULL,
            created_at TEXT NOT NULL,
            expires_at TEXT NOT NULL,
            used_at TEXT,
            revoked_at TEXT
        )
        """
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_invites_tenant_email ON invites(tenant_id, email)"
    )

    conn.execute(
        f"""
        CREATE TABLE IF NOT EXISTS watchlist (
            id {_id_column_sql(conn)},
            tenant_id TEXT NOT NULL,
            chain TEXT NOT NULL,
            address TEXT NOT NULL,
            label TEXT NOT NULL,
            created_at TEXT NOT NULL,
            created_by TEXT NOT NULL,
            last_seen_at TEXT,
            last_score INTEGER,
            alert_on_activity INTEGER NOT NULL DEFAULT 1,
            UNIQUE(tenant_id, chain, address)
        )
        """
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_watchlist_tenant ON watchlist(tenant_id)"
    )


def _migration_002_alerts_and_incidents(conn: DatabaseConnection) -> None:
    conn.execute(
        f"""
        CREATE TABLE IF NOT EXISTS alert_events (
            id {_id_column_sql(conn)},
            tenant_id TEXT NOT NULL,
            created_at TEXT NOT NULL,
            trigger TEXT NOT NULL,
            chain TEXT NOT NULL,
            address TEXT NOT NULL,
            score INTEGER NOT NULL,
            risk_level TEXT NOT NULL,
            title TEXT NOT NULL,
            body TEXT NOT NULL,
            acknowledged INTEGER NOT NULL DEFAULT 0
        )
        """
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_alert_events_tenant ON alert_events(tenant_id, created_at)"
    )

    for column_name, ddl in [
        ("alert_type", "TEXT NOT NULL DEFAULT 'score_threshold'"),
        ("severity", "TEXT NOT NULL DEFAULT 'warning'"),
        ("prev_score", "INTEGER"),
        ("acknowledged_at", "TEXT"),
        ("resolved_at", "TEXT"),
        ("incident_id", "INTEGER"),
    ]:
        if not _column_exists(conn, "alert_events", column_name):
            conn.execute(f"ALTER TABLE alert_events ADD COLUMN {column_name} {ddl}")

    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_alert_events_severity ON alert_events(tenant_id, severity, acknowledged)"
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_alert_events_feed ON alert_events(tenant_id, id)"
    )

    conn.execute(
        f"""
        CREATE TABLE IF NOT EXISTS incidents (
            id          {_id_column_sql(conn)},
            tenant_id   TEXT NOT NULL,
            title       TEXT NOT NULL,
            description TEXT NOT NULL DEFAULT '',
            severity    TEXT NOT NULL DEFAULT 'warning',
            status      TEXT NOT NULL DEFAULT 'open',
            alert_count INTEGER NOT NULL DEFAULT 0,
            created_at  TEXT NOT NULL,
            updated_at  TEXT NOT NULL,
            resolved_at TEXT,
            created_by  TEXT NOT NULL DEFAULT ''
        )
        """
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_incidents_tenant_updated ON incidents(tenant_id, updated_at)"
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_incidents_tenant_status ON incidents(tenant_id, status, updated_at)"
    )

    conn.execute(
        f"""
        CREATE TABLE IF NOT EXISTS webhooks (
            id {_id_column_sql(conn)},
            tenant_id TEXT NOT NULL,
            url TEXT NOT NULL,
            events TEXT NOT NULL,
            created_at TEXT NOT NULL,
            active INTEGER NOT NULL DEFAULT 1
        )
        """
    )
    conn.execute(
        "CREATE INDEX IF NOT EXISTS idx_webhooks_tenant ON webhooks(tenant_id)"
    )


